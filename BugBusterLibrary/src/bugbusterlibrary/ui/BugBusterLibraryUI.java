    private void signUp(){
        String firstName = RegName.getText();
        String lastName = RegLname.getText();
        String email = regEmail.getText();
        String role = "";
        if(regRoleStudent.isSelected()){
            role = "student";
        } 
        else if(regRoleAdmin.isSelected()){
            role = "admin";
        }        
        String Username = RegUsername.getText();
        String Password = RegPassword.getText();
        String Re_enter_password = RegReEnterpassword.getText();
        if(!"".equals(firstName) && !"".equals(lastName) && !"".equals(Username) && !"".equals(Password) && !"".equals(role)){
            if(!email.contains(UWC_EDOMAIN)){
                JOptionPane.showMessageDialog(null, "Email domain not allowed or invalid");
            }
            else if(Password!=null){
                UserDao userDao = new UserDao();
                userDao.persist(new User(Username, firstName, lastName, PassCrypt.hash(Password), email, role, new Date()));
                JOptionPane.showMessageDialog(null, "Registered!");
                RegistrationPane.setVisible(false);
                LogInPane.setVisible(true);
            }
            else{
                JOptionPane.showMessageDialog(null, "Passwords don't match!");
            }
        }
        else{
            JOptionPane.showMessageDialog(null, "Enter all fields!");
        }
    }
    
    private void logIn(){
        String Logname = LogInUsername.getText();
        String Logpass = LogInPassword.getText();
        String role = "";
        if(LogInRoleStudent.isSelected()){
            role = "student";
        } 
        else if(LogInRoleAdmin.isSelected()){
            role = "admin";
        }
        try{
            UserDao userDao = new UserDao();
            User user = userDao.findByUsername(Logname);
            if(user.getUsername().equals(Logname) && PassCrypt.validate(Logpass, user.getPassword()) && user.getRole().equals(role)){
                JOptionPane.showMessageDialog(null, "Logging in!");
                LogInPane.setVisible(false);
                if("student".equals(role)){                  
                    StudentMenuPane.setVisible(true);
                    UserLoggedInName.setText(Logname);
                }
                else if("admin".equals(role)){
                    AdminMenuPane.setVisible(true);
                    AdminLoggedInName.setText(Logname);
                }
            }
            else{
                JOptionPane.showMessageDialog(null, "Incorect username or password, or role!");
            }
        }catch(NoResultException e){
            JOptionPane.showMessageDialog(null, "No such user!");
        }
    }
    
